public function countBynomr(string $nomr): array
{
    $qb = $this->createQueryBuilder('r')
        ->select("r.$nomr as value, COUNT(r.id) as count")
        ->groupBy("r.$nomr")
        ->getQuery();

    return $qb->getResult();
}
public function SortByEmailr()
{
    return $this->createQueryBuilder('e')
        ->orderBy('e.emailr','ASC')
        ->getQuery()
        ->getResult()
        ;
}

public function findByemail( $emailr)
{
    return $this-> createQueryBuilder('e')
        ->andWhere('e.emailr LIKE :emailr')
        ->setParameter('emailr','%' .$emailr. '%')
        ->getQuery()
        ->execute();
}


